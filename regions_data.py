"""regions_data.py — единый источник правды для регионов Казахстана.

Используется и в REST-эндпоинтах (main.py), и в MCP-сервере (services/mcp_server.py),
чтобы не дублировать список в нескольких местах.
"""

REGION_MAPPING = {
    "алматин": "Алматинская",
    "алмат": "Алматы",
    "астан": "Астана",
    "нур-султан": "Астана",
    "шымкент": "Шымкент",
    "акмол": "Акмолинская",
    "актюб": "Актюбинская",
    "атырау": "Атырауская",
    "восточно": "ВКО",
    "жамбыл": "Жамбылская",
    "западно": "ЗКО",
    "караган": "Карагандинская",
    "костан": "Костанайская",
    "кызылорд": "Кызылординская",
    "мангист": "Мангистауская",
    "павлодар": "Павлодарская",
    "северо": "СКО",
    "туркестан": "Туркестанская",
    "абай": "Абай",
    "жетысу": "Жетісу",
    "улытау": "Ұлытау",
}


def normalize_region_name(raw_reg: str | None) -> str:
    """Приводит сырой регион из БД к имени, которое ждёт фронтенд."""
    if not raw_reg:
        return "Астана"
    raw_lower = str(raw_reg).lower()
    for key, fine_name in REGION_MAPPING.items():
        if key in raw_lower:
            return fine_name
    return "Астана"
